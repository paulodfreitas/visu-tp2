var maxValue = 0;
var minDate  = (new Date()).getTime(); //all our data is in past.
var maxDate  = 0;

function simpleMax(a, b) { return a > b ? a : b; }
function simpleMin(a, b) { return a < b ? a : b; }

function prepareDataSets(dataSets) {
    var importLayers = [];
    var exportLayers = [];

    dataSets.forEach(function (dataSet) {
        var importLayer = [];
        var exportLayer = [];

        dataSet.forEach(function(row) {
            var date      = (new Date(row[0])).getTime();
            var direction = row[1];
            var value     = parseInt(row[2]);

            var entryToAppend  = direction === DIRECTION.EXPORT ? exportLayer : importLayer;
            entryToAppend.push({"x" : date, "y" : value});

            maxValue = simpleMax(maxValue, value);
            minDate  = simpleMin(minDate, date);
            maxDate  = simpleMax(maxDate, date);
        });

        importLayers.push(importLayer);
        exportLayers.push(exportLayer);
    });

    return {"import" : importLayers, "export" : exportLayers};
}

var absoluteLayers = prepareDataSets(dataSets);

function convertIntoPercentLayers(absoluteLayers) {
    var percentLayers = JSON.parse(JSON.stringify(absoluteLayers));

    var xAxisLength = absoluteLayers[0].length;
    var nrLayers = absoluteLayers.length;

    for (var i = 0; i < xAxisLength ; i++) {
        var sumOfValues = 0;
        for (var j = 0; j < nrLayers; j++) {
            sumOfValues += absoluteLayers[j][i]["y"];
        }

        for (var k = 0; k < nrLayers; k++) {
            percentLayers[k][i]["y"] = (100 * absoluteLayers[k][i]["y"]) / sumOfValues;
        }
    }

    return percentLayers;
}

var percentLayers = {
    "import" : convertIntoPercentLayers(absoluteLayers.import),
    "export" : convertIntoPercentLayers(absoluteLayers.export)
};

function initImportExport(width, height) {
    var stack = d3.layout.stack().offset("stack").values(function(d) { return d; });
    var layers = {
        "absolute" : {
            "import" : stack(absoluteLayers.import),
            "export" : stack(absoluteLayers.export)
        },
        "percent"  : {
            "import" : stack(percentLayers.import),
            "export" : stack(percentLayers.export)
        }
    };

    function getAbsoluteArea() {
        var x = d3.scale.linear()
            .domain([minDate, maxDate])
            .range([0, width]);

        var y = d3.scale.linear()
            .domain([0, d3.max(layers.absolute.import.concat(layers.absolute.export), function (layer) {
                return d3.max(layer, function (d) {
                    return d.y0 + d.y;
                });
            })])
            .range([height, 0]);

        return d3.svg.area()
            .x(function (d) {
                return x(d.x);
            })
            .y0(function (d) {
                return y(d.y0);
            })
            .y1(function (d) {
                return y(d.y0 + d.y);
            });
    }

    function getPercentArea() {
        var x = d3.scale.linear()
            .domain([minDate, maxDate])
            .range([0, width]);

        var y = d3.scale.linear()
            .domain([0, 100])
            .range([height, 0]);

        return d3.svg.area()
            .x(function (d) {
                return x(d.x);
            })
            .y0(function (d) {
                return y(d.y0);
            })
            .y1(function (d) {
                return y(d.y0 + d.y);
            });
    }

    var areas = {
        "absolute" : getAbsoluteArea(),
        "percent"  : getPercentArea()
    };

    function drawGraph(selector, direction) {
        var svg = d3.select(selector).append("svg")
            .attr("width", width)
            .attr("height", height);

        svg.selectAll("path")
            .data(layers.absolute[direction])
            .enter().append("path")
            .attr("d", areas.absolute)
            .style("fill", function(d, i) { return auxMap[i].color; })
            .append("title").text(function(d, i) {
            return auxMap[i].name;
        });
    }

    drawGraph("#import_by_continent", "import");
    drawGraph("#export_by_continent", "export");
}